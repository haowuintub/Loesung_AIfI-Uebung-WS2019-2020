package uebung1;

class Calculator {

    public static void main(String[] args) {
        int x = 5 ;
        int y = 9 ;

        int sum = x + y ;
        int diff = x - y ;

        System.out.println(sum);
        System.out.println(diff);


        System.out.println(x + " + " + y + " = " + sum);
        System.out.println(x + " - " + y + " = " + diff);

    }

}
